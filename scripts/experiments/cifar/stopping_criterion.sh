#!/usr/bin/env bash
set -euo pipefail

SEED="${1:-42}"
METHODS="${METHODS:-hamu-q hamu-u}"
HARD_PROBABILITY="${HARD_PROBABILITY:-0.5}"
CONSTRAINT_RATIO="${CONSTRAINT_RATIO:-0.5}"
NUM_TRAIN_EPOCHS="${NUM_TRAIN_EPOCHS:-25}"
DATASET_REPEAT="${DATASET_REPEAT:-25}"

for method in $METHODS; do
  METHOD="$method" \
  HARD_PROBABILITY="$HARD_PROBABILITY" \
  CONSTRAINT_RATIO="$CONSTRAINT_RATIO" \
  NUM_TRAIN_EPOCHS="$NUM_TRAIN_EPOCHS" \
  DATASET_REPEAT="$DATASET_REPEAT" \
  STOP_ON_STOPPING_CRITERION="${STOP_ON_STOPPING_CRITERION:-1}" \
    "$(dirname "$0")/hamu_unlearn.sh" "$SEED"
done
